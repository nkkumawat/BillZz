    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
        Date date = new Date();
      
        String customer_name = customerName.getText();
        String locality_txt  = locality.getText();
        String bill_address = billAddress.getText();
        String e_mail = email.getText();
        String mobile_no = mobileNumber.getText();
        String last_bill_date = dateFormat.format(date); // todays date
        String last_bill_amt = 0 +"";
        String last_pay_time = dateFormat.format(date);
        String unbilled_amt = 0+"";
        
        if(customer_name.equals("") || locality_txt.equals("") || bill_address.equals("")|| 
                e_mail.equals("") ||mobile_no.equals("") || last_bill_date.equals("") || last_bill_amt.equals("")|| last_pay_time.equals("")
                || unbilled_amt.equals("")) {
            javax.swing.JOptionPane.showMessageDialog(null, "Fill All Feilds", "InfoBox:",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }else{
            try{
                Statement stmt = SqlConnection.getStat();
                String insertCustomer = "insert into customer(full_name, locality, email, mobile , billing_address, last_bill_date, last_bill_amount, last_pay_time, unbilled_amount)\n"
                +"values('"+customer_name+"' , '"+locality_txt+"' , '"+e_mail+"' , '"+mobile_no+"' , '"+bill_address+"' , '"+last_bill_date+"\n"
                        + "' , '"+last_bill_amt+"' , '"+last_pay_time+"' , '"+unbilled_amt+"')";
                stmt.execute(insertCustomer);
                this.setVisible(false);
                javax.swing.JOptionPane.showMessageDialog(null, "Customer Added Successfully", "InfoBox:",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }catch(Exception e){
                javax.swing.JOptionPane.showMessageDialog(null, e.toString(), "InfoBox:",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_submitActionPerformed

